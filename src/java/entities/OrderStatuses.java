package entities;

import java.io.Serializable;
import javax.persistence.Basic;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.NamedQueries;
import javax.persistence.NamedQuery;
import javax.persistence.Table;
import javax.validation.constraints.NotNull;
import javax.validation.constraints.Size;
import javax.xml.bind.annotation.XmlRootElement;

/**
 *
 * @author WD
 */
@Entity
@Table(name = "order_statuses")
@XmlRootElement
@NamedQueries({
    @NamedQuery(name = "OrderStatuses.findAll", query = "SELECT o FROM OrderStatuses o")
    , @NamedQuery(name = "OrderStatuses.findById", query = "SELECT o FROM OrderStatuses o WHERE o.id = :id")
    , @NamedQuery(name = "OrderStatuses.findByTitle", query = "SELECT o FROM OrderStatuses o WHERE o.title = :title")})
public class OrderStatuses implements Serializable {

    private static final long serialVersionUID = 1L;
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    @Basic(optional = false)
    @Column(name = "id")
    private Integer id;
    @Basic(optional = false)
    @NotNull
    @Size(min = 1, max = 100)
    @Column(name = "title")
    private String title;

    public OrderStatuses() {
    }

    public OrderStatuses(Integer id) {
        this.id = id;
    }

    public OrderStatuses(Integer id, String title) {
        this.id = id;
        this.title = title;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    @Override
    public int hashCode() {
        int hash = 0;
        hash += (id != null ? id.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        // TODO: Warning - this method won't work in the case the id fields are not set
        if (!(object instanceof OrderStatuses)) {
            return false;
        }
        OrderStatuses other = (OrderStatuses) object;
        if ((this.id == null && other.id != null) || (this.id != null && !this.id.equals(other.id))) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "entities.OrderStatuses[ id=" + id + " ]";
    }
    
}
